export function getKycStatusStyle(kycStatus) {
  switch (kycStatus) {
    case 'Done':
      return 'rounded-3xl text-sm w-[80%] flex items-center justify-center text-[Lato] text-[#27A607] bg-[#EAFFE0]';
    case 'Pending':
      return 'rounded-3xl text-sm w-[80%] flex items-center justify-center text-[Lato] text-[#FF4444] bg-[#FFE9E9]';
    case 'In Progress':
      return 'rounded-3xl text-sm w-[80%] flex items-center justify-center text-[Lato] text-[#DBA00A] bg-[#FFF2D0]';
    case 'Rejected':
      return 'rounded-3xl text-sm w-[80%] flex items-center justify-center text-[Lato] text-[#8E8E8E] bg-[#F1F1F1]';
    default:
      return '';
  }
}

